    public Assignment getSubmitted(Assignment asgn) {
        
        for( Assignment temp : allSubmissions) {
            String asgnName = temp.getName().substring(temp.getName().lastIndexOf("_") + 1);
            if(asgn.getName().equals(asgnName)) {
                return temp;
            }
        }
        return null;
    }
